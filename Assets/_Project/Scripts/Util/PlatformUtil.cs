using UnityEngine;
using System.Collections;

public class PlatformUtil {

	public static string getPlatform()
	{
		string platform = "pc";
		if(Application.platform==RuntimePlatform.Android)
		{
			platform="android";
		}
		else if(Application.platform==RuntimePlatform.IPhonePlayer)
		{
			platform="ios";
		}
		return platform;
	}
}
